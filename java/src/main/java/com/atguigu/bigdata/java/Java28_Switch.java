package com.atguigu.bigdata.java;

public class Java28_Switch {
    public static void main(String[] args) throws Exception {

        int age = 40;

        switch ( age ) {
            default:
                System.out.println(0);
                break;
            case 10 :
                System.out.println(10);
                break;
            case 20 :
                System.out.println(20);
                break;
            case 30 :
                System.out.println(30);
                break;

        }
    }



}



